# Strings in Python 

fruit = "pomogranate"
print(fruit)
print(type(fruit))
print(len(fruit))

institute = "Dublin Business School"
print(institute)
print(institute[0])
print(institute[7])
print(institute[16])

# slicing the string
print(institute[7:15])          # slicing a string using string index

print(institute[:7])
print(institute[16:])
